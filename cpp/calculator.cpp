#include<iostream>

using namespace std;

int main()
{
    int a,b,sum, sub, mul, div;
    cout<<"Enter two Numbers:"<<endl;
    cin>>a>>b;
    sum=a+b;
    sub=a-b;
    mul=a*b;
    div=a/b;
    cout<<"Sum is: "<<sum<<endl;
    cout<<"Sub is: "<<sub<<endl;
    cout<<"Mul is: "<<mul<<endl;
    cout<<"Div is: "<<div<<endl;
    return 0;
}
